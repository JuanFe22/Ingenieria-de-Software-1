    void show_hospitals(){
        String query = "SELECT * FROM tb_hospitals";
        try{
            cn = con.getConnection();
            st = cn.createStatement();
            rs = st.executeQuery(query);
            Object[] hospital = new Object[3];
            model = (DefaultTableModel)tbl_hospitals.getModel();
            while(rs.next()){
                hospital[0] = rs.getInt("id");
                hospital[1] = rs.getString("hospital");
                hospital[2] = rs.getString("address");
                model.addRow(hospital);
            }
            tbl_hospitals.setModel(model);
        }catch(SQLException e){
            JOptionPane.showConfirmDialog(this, "No se encontraron registros de hospitales");
        }
    }
    
    void add_hospitals(){
        //No se a帽ade el id
        String hospital = txt_hospital.getText();
        String address = txt_address.getText();
        if (hospital.isEmpty() || address.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Faltan campos por digilenciar");
        }else{
            String query = "INSERT INTO tb_hospitals (`hospital`, `address`) VALUES ('"+hospital+"','"+address+"')";
            try{
            cn = con.getConnection();
            st = cn.createStatement();
            st.executeUpdate(query);
            JOptionPane.showMessageDialog(this, "Se ha creado el registro con exito");
            clear_rows_table();
            show_hospitals();
            }catch(SQLException e){
                JOptionPane.showConfirmDialog(this, "No se puede crear el registro");
            }
        }
    }
    
    void clear_rows_table(){
        for (int i = 0; i < tbl_hospitals.getRowCount(); i++) {
            model.removeRow(i);
            i -= 1;
        }
        txt_idHospital.setText("");
        txt_hospital.setText("");
        txt_address.setText("");
    }



